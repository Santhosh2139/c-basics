//printing all arithmetic operations
#include<stdio.h>
void main()
{
	int a, b, s, d, p, q, r;
	a = 50;
	b = 35;
	
	s=a+b;
	
	printf("sum of %d and %d is %d", a, b, s);
	
	d=a-b;
	
	printf("\nsum of %d and %d is %d", a, b, d);
	
	q=a/b;
	
	printf("\nsum of %d and %d is %d", a, b, q);
	
	p=a*b;
	
	printf("\n\t\t\t\t\t sum of %d and %d is %d", a, b, p);
		
	r=a%b;
		
	printf("\nsum of %d and %d is %d", a, b, r);
}
